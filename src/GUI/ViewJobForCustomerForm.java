    public void JobTable() {
        try
        {
            conn = MysqlConnect.ConnectDB();
            // display jobs with additional data from other tables using left join
            pst =conn.prepareStatement("SELECT job_id AS ID, deadline, complete, priority, quantity, job_description AS 'job description', special_instructions AS 'special instructions', time_completed AS 'time completed', "
                    + "time_created AS 'time created' "
                    + "FROM job "
                    + "LEFT JOIN Job_Task ON Job.job_id = Job_Task.Job_job_id "
                    + "LEFT JOIN Task ON Task.task_id = Job_Task.Task_task_id "
                    + "LEFT JOIN Staff ON Staff.staff_id = Job_Task.Staff_staff_id");
            rs = pst.executeQuery();
            table_job.setModel(DbUtils.resultSetToTableModel(rs));
            conn.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
