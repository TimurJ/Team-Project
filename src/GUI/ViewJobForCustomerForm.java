    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        try
        {
            conn = MysqlConnect.ConnectDB();
            // display table data which is linked to job id that matches the value of the text field
            pst =conn.prepareStatement("SELECT job.job_id as ID, Task.task_id AS 'task', Task.location AS 'department', Job_Task.start_time AS 'start time', Task.duration AS 'time taken (min)', Staff.name AS 'completed by', Task.shelf_slot AS 'shelf on completion' "
                    + "FROM Job "
                    + "LEFT JOIN Job_Task ON Job.job_id = Job_Task.Job_job_id "
                    + "LEFT JOIN Task ON Task.task_id = Job_Task.Task_task_id "
                    + "LEFT JOIN Staff ON Staff.staff_id = Job_Task.Staff_staff_id "
                    + "WHERE job_id = '"+txt_search.getText()+"'");
            rs = pst.executeQuery();
            table_job.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_searchActionPerformed

