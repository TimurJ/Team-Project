    // attempt to calculate the change that is needed to be given to customer
    // by changing text values into a float and finding the change by
    // taking away total from the amount given
    // the float value holding change was then converted back to string
    // to be displayed in a text field, however it did not perform as expected
    private void btn_changeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_changeActionPerformed
        float amount = Float.parseFloat(txt_amount.getText());
        float total = Float.parseFloat(txt_total.getText());
        
        float change = amount - total;
        String end = String.valueOf(change);
        txt_change.setText(end);
    }//GEN-LAST:event_btn_changeActionPerformed

    private void btn_doneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_doneActionPerformed
        conn = MysqlConnect.ConnectDB();
        // update job to paid status where job id matches value within text field
        String Sql = "UPDATE job SET paid = 1 WHERE job_id = '"+txt_jobID.getText()+"'";
        try {
            pst = conn.prepareStatement(Sql); // prepare statement
            pst.executeUpdate(); // execute query
            // send to office home form
            OfficeManagerHomeForm omh = new OfficeManagerHomeForm();
            omh.setVisible(true);
            this.dispose();    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e); // handle exceptions
        }
    }//GEN-LAST:event_btn_doneActionPerformed

