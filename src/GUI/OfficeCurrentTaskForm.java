    public void TasksTable() {
        try
        {
            System.out.println(txt_jobID.getText());
            conn = MysqlConnect.ConnectDB();
            // show list of tasks with added data taken from job_task to highlight additional features, such as when the task was created etc.
            pst =conn.prepareStatement("SELECT task_id AS 'ID', task.price, task.description, task.location, task.shelf_slot AS 'shelf slot', task.duration, "
                    + "job_task.start_time AS 'start time', job_task.complete, job_task.commenced "
                    + "FROM job_task "
                    + "INNER JOIN task ON task.task_id = job_task.task_task_id "
                    + "WHERE job_task.job_job_id = '"+txt_jobID.getText()+"'");
            rs = pst.executeQuery();
            table_tasks.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
