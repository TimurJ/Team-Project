    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        int row = table_tasks.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)table_tasks.getModel();

        String selected = model.getValueAt(row, 0).toString();

            if (row >= 0) {

                model.removeRow(row);

                try {
                    conn = MysqlConnect.ConnectDB();
                    // deleted where foreign key of task_id from the selected row is dervied
                    pst = conn.prepareStatement("DELETE FROM job_task WHERE task_task_id='"+selected+"' ");
                    pst.executeUpdate();
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e);
                }           
            }
    }//GEN-LAST:event_btn_removeActionPerformed

    private void btn_markActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_markActionPerformed
        int row = table_tasks.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)table_tasks.getModel();

        String selected = model.getValueAt(row, 6).toString();
        LocalDateTime local = LocalDateTime.now();
        if (row >= 0) {

            try {
                conn = MysqlConnect.ConnectDB();
                // update value of complete to true and set end time as the current date/time where the start time equals the value of the recorded start time in the row selected
                pst = conn.prepareStatement("UPDATE job_task SET complete = 1, end_time = '"+local.toString()+"' WHERE start_time = '"+ selected +"' ");
                pst.executeUpdate();
                TasksTable();
            }
            catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_btn_markActionPerformed

