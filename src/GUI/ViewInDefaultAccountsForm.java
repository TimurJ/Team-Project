    private void btn_reactivateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_reactivateActionPerformed
        int row = table_customer.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)table_customer.getModel();

        String selected = model.getValueAt(row, 0).toString();

            if (row >= 0) {

                model.removeRow(row);

                try {
                    conn = MysqlConnect.ConnectDB();
                    // mark customer as not default where customer id matches the value of the selected id within the row
                    pst = conn.prepareStatement("UPDATE Customer SET in_default = 0 WHERE customer_id='"+selected+"' ");
                    pst.executeUpdate();
                    
                    OfficeManagerHomeForm omh = new OfficeManagerHomeForm();
                    omh.setVisible(true);
                    this.dispose();
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e);
                }           
            }
    }//GEN-LAST:event_btn_reactivateActionPerformed

