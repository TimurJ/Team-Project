    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        try
        {
            conn = MysqlConnect.ConnectDB();
            // display in default accounts that match the customer id which matches the value of the text field
            pst =conn.prepareStatement("SELECT customer_id AS ID, name, email, address, contact_number AS 'contact number', contact_person AS 'contact person' "
                    + "FROM customer "
                    + "WHERE customer_id = '"+txt_search.getText()+"' AND in_default = 1");
            rs = pst.executeQuery();
            table_customer.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_searchActionPerformed

