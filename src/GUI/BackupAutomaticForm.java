    void showDate() {
        java.util.Date d = new java.util.Date();
        SimpleDateFormat s = new SimpleDateFormat("dd-MM-yyyy"); // set format for date
        label_date.setText(s.format(d)); // set label to date
    }

    void showTime() {
        new Timer(0, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                java.util.Date d = new java.util.Date();
                SimpleDateFormat s = new SimpleDateFormat("hh-mm-ss a"); // set format for time
                label_time.setText(s.format(d)); // set time to label for display
            }

        }
        ).start();
    }
    
