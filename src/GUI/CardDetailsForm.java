    private void btn_completeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_completeActionPerformed
        conn = MysqlConnect.ConnectDB();
        String Sql = "insert into card_details(card_number, expiry_date, cvv, card_type) values (?, ?, ?, ?)"; // insert card details 
        
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd"); // set format to yyy-mm-dd
        String date = sdf.format(date_expire.getDate()); // get date that has been selected
        date_expire.setDateFormatString(date); // set the date to new format
        try {
            pst = conn.prepareStatement(Sql);
            // values to be entered to respective attributes
            pst.setString(1, txt_number.getText());
            pst.setString(2, date_expire.getDateFormatString());
            pst.setString(3, txt_digits.getText());
            pst.setString(4, txt_type.getText());

            int i = pst.executeUpdate(); // execute sql
            JOptionPane.showMessageDialog(this, "Payment successful");

            btn_mark.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_completeActionPerformed

