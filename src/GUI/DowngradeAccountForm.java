            pst =conn.prepareStatement("SELECT valued_id AS ID, type_discount AS 'discount agreed', customer.name, customer.email, customer.address,"
                    + "customer.contact_number AS 'contact number', customer.contact_person AS 'contact person'"
                    + "FROM valued_customer "
                    + "INNER JOIN customer ON valued_customer.customer_customer_id = customer.customer_id "
                    + "WHERE valued_id = '"+txt_search.getText()+"'");
