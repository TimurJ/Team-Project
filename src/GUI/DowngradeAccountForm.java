    private void btn_downgradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_downgradeActionPerformed
        // get selected row and assign it to int row
        int row = table_valuedCust.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)table_valuedCust.getModel();
        // get value at row and make it a string
        String selected = model.getValueAt(row, 0).toString();

            if (row >= 0) {

                model.removeRow(row);

                try {
                    conn = MysqlConnect.ConnectDB();
                    // remove selected row 
                    pst = conn.prepareStatement("DELETE FROM valued_customer WHERE valued_id='"+selected+"' ");
                    pst.executeUpdate();
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e); // handle exception 
                }           
            }
    }//GEN-LAST:event_btn_downgradeActionPerformed

