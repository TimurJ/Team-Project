    private void btn_restoreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_restoreActionPerformed
        String user = "root"; // username for mysql server
        String password = "rpgdragon"; // password for database
        String[] restoreCmd = new String[]{"C:/Program Files/MySQL/MySQL Server 5.7/bin/mysql.exe", "--user="+user,"--password="+password,"-e","source "+path}; //restore where path selected using username and password

        Process process;

        try {
            process = Runtime.getRuntime().exec(restoreCmd); // execute restore command
            int procCom = process.waitFor(); // wait for process

            if (procCom == 0) {
                txt_label1.setText("Restore Successful");
            } else {
                txt_label1.setText("Restore Failed");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e); // handle exception
        }
    }//GEN-LAST:event_btn_restoreActionPerformed

