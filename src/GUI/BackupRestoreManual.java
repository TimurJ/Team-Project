    private void btn_backupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_backupActionPerformed
        Process p = null;
        
        try {
            Runtime runtime = Runtime.getRuntime();
            // find file to where database is held
            p = runtime.exec("C:/Program Files/MySQL/MySQL Server 5.7/bin/mysqldump.exe -uroot -prpgdragon --add-drop-database -B bapers_database -r" + path);
            
            int processComplete = p.waitFor(); // wait until process is complete
            if (processComplete == 0) {
                txt_label.setText("Backup Successful");
            } else {
                txt_label.setText("Backup Failed");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e); // handle exception
        }
    }//GEN-LAST:event_btn_backupActionPerformed

    private void btn_browseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_browseActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        String date = new SimpleDateFormat("yyyy-mm-dd").format(new Date()); // set format for date
        
        try {
            File f = fc.getSelectedFile(); // get file path
            path = f.getAbsolutePath();
            path = path.replace('\\', '/'); // replace back slashes for forward slashes
            path = path + "_" + date + ".sql"; // add date and .sql to end of path
            txt_filePath.setText(path); // set file path to text field
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_browseActionPerformed

